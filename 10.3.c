#include <stdio.h>
#include<string.h>

int main() {
    int vowel = 0;
    int digits = 0;
    int constants = 0;
    int spaces = 0;
    char sen[50];
    printf("enter your sentence:");
    fgets(sen , sizeof(sen) , stdin);
    int len = strlen(sen);
   char vowels[] = "aeiouAEIOU";
    for(int i=0 ; i<len;i++){
        if(strchr(vowels , sen[i]) != NULL){
            vowel++;
        }
        else{
            constants++;
        }
    }
    for(int i=0;i<len;i++){
        if(sen[i] >='0' && sen[i]<='9'){
            digits++;
        }
    }
    for(int i=0;i<len;i++){
        if(sen[i] == ' '){
            spaces++;
        }
    }
    printf("stats:\n");
    printf("Digits:%d\n" , digits);
    printf("total characters:%d\n" , len);
    printf("Vowels:%d\n" , vowel);
    printf("constants:%d\n" , constants);
    printf("spaces:%d\n" , spaces);
    

    return 0;
}
